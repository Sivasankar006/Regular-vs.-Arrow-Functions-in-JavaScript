// Function Expression:

const multiply = function(a, b) {
    return a * b;
  }
  console.log(multiply(1, 3),"Function Expression:"); // Output: 3
  

// Arrow Function:

const mul = (a, b) => a * b;
console.log(mul(1, 2),"Arrow Function:"); // Output: 2

const square = a => a * a;
console.log(square(2),"Arrow Function:"); // Output: 4
